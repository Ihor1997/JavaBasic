package lesson2;

public class SwitchWork {

    public static void main(String[] args) {
        int value = 4;
        switch (value){
            case 0:
                System.out.println("case 0");
                break;
            case 1:
                System.out.println("case 2");
                break;

            case 4:
                System.out.println("case 4");
        }

    }
}
